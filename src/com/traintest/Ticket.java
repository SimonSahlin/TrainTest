package com.traintest;

public class Ticket {
	
	final private int discountSingle = 20;
	final private int discountMonth = 450;
	final private int normalSingle = 35;
	final private int normalMonth = 600;
	
	
	public int getDiscountSingle() {
		return discountSingle;
	}
	public int getDiscountMonth() {
		return discountMonth;
	}
	public int getNormalSingle() {
		return normalSingle;
	}
	public int getNormalMonth() {
		return normalMonth;
	}
	
}
